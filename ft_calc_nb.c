#include "ft_printf.h"


int ft_calc(long n)
{
 int i;
 
 i = 0;
 if (n == 0)
  return (1);
 if (n < 0)
 {
  i++;
 }
 while (n != 0)
 {
  n = n / 10;
  i++;
 }
 return (i);
}